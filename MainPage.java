    private void refreshTable() {
        tableModel.setRowCount(0);

        for (Barang barang : listBarang) {
            String[] data = {
                barang.getIdBarang(),
                barang.getNamaBarang(),
                barang.getKategori(),
                Integer.toString(barang.getJumlah()),
                Integer.toString(barang.getHarga())
            };

            tableModel.addRow(data);
        }
    }
    
